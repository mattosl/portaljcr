package br.com.grupojcr.dto;

public class ZMDRMFLUIGDTO {
	
	private Integer idColigada;
	private Integer idMovimento;
	private Integer idCnt;
	private Integer idFluig;
	
	public Integer getIdColigada() {
		return idColigada;
	}
	public void setIdColigada(Integer idColigada) {
		this.idColigada = idColigada;
	}
	public Integer getIdMovimento() {
		return idMovimento;
	}
	public void setIdMovimento(Integer idMovimento) {
		this.idMovimento = idMovimento;
	}
	public Integer getIdCnt() {
		return idCnt;
	}
	public void setIdCnt(Integer idCnt) {
		this.idCnt = idCnt;
	}
	public Integer getIdFluig() {
		return idFluig;
	}
	public void setIdFluig(Integer idFluig) {
		this.idFluig = idFluig;
	}

}
